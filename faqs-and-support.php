				<h1>Passport d&amp;o</h1>
				<h4>Why should I take out a separate policy when the<br> company of which I am a Director already has<br> Directors’ and Officers’ Insurance?</h4>
				<p>Your company’s Directors’ and Officers’ insurance policy may not respond to a claim against you for host of reasons such as:</p>
				
				<ul>
					<li>Insolvency exclusion clause.</li>
					<li>The company’s Directors’ and Officers’ limit is reached because of an existing claim.</li>
				</ul>
				
				<p>Therefore for the rest of the year, there may not be any limit of indemnity to respond to a new claim.</p>
				<p>In both instances, Passport D&O would step up to protect your interests in the event of a claim against you.</p>
				
				<div class="hr blue"></div>
				
				<h4>who can get cover?</h4>
				<p>A Director or an Officer who is a subscriber of Women on Boards.</p>
				
				<div class="hr blue"></div>
				
				<h4>what isn’t covered?</h4>
				<ul>
					<li>Claims brought in the jurisdiction and courts of the United States of America are excluded.</li>
					<li>Claims arising from the offering of securities of any Company where a Board Position is held are excluded.</li>
					<li>Coverage for Directors’ and Officers’ positions held on Exchange Traded Entities is not automatic. However, details can be submitted to the Underwriters for consideration. 
An additional premium may apply.</li>
				</ul>
				
				<p class="fine">Full particulars of the Passport D&amp;O coverage please refer to the full Passport D&amp;O Policy wording. <a href="/downloads/Passport_D&O_Policy.pdf" target="_blank">Download the Passport D&amp;O Policy</a>.</p>
				
				<div class="hr blue"></div>
				
				<h4>What is an Exchange Traded Entity?</h4>
				<p>An Exchange Traded Entity is any entity whose securities are publicly traded (wholly or in part) on a stock exchange in any jurisdiction.</p>
			
